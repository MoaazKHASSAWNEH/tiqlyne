import fs from 'node:fs';
import process from 'node:process';

const outputPath = 'website/src/data/packageVersions.ts';
const checkOnly = process.argv.includes('--check');

const packageDefinitions = [
  {
    path: 'packages/motion-core/package.json',
    name: '@tiqlyne/motion-core',
    description: 'Platform-independent engine core'
  },
  {
    path: 'packages/motion-web/package.json',
    name: '@tiqlyne/motion-web',
    description: 'Browser driver using the Web Animations API'
  },
  {
    path: 'packages/motion-pack-basic/package.json',
    name: '@tiqlyne/motion-pack-basic',
    description: 'Official basic motion definitions'
  }
];

function readJson(path) {
  return JSON.parse(fs.readFileSync(path, 'utf8'));
}

const packages = packageDefinitions.map((definition) => {
  const manifest = readJson(definition.path);

  if (manifest.name !== definition.name) {
    throw new Error(
      `Package name mismatch in ${definition.path}: expected ${definition.name}, got ${manifest.name}`
    );
  }

  if (typeof manifest.version !== 'string' || manifest.version.length === 0) {
    throw new Error(`Missing version in ${definition.path}`);
  }

  return {
    ...definition,
    version: manifest.version
  };
});

function quote(value) {
  return `'${value}'`;
}

function generatePackageVersionsFile() {
  const versionEntries = packages
    .map((item) => `  ${quote(item.name)}: ${quote(item.version)}`)
    .join(',\n');

  const rows = packages
    .map(
      (item) => `  {
    name: ${quote(item.name)},
    description: ${quote(item.description)}
  }`
    )
    .join(',\n');

  return `// This file is generated by scripts/sync-package-versions.mjs.
// Do not edit it manually.

export const packageVersions = {
${versionEntries}
} as const;

export type TiqlynePackageName = keyof typeof packageVersions;

export const packageVersionRows: Array<{
  name: TiqlynePackageName;
  description: string;
}> = [
${rows}
];

const uniqueVersions = Array.from(new Set(Object.values(packageVersions)));

export const packageVersionSummary =
  uniqueVersions.length === 1
    ? ` + "`All public packages are currently ${uniqueVersions[0]}`" + `
    : 'Public packages use independent versions';
`;
}

const expectedContent = generatePackageVersionsFile();
const currentContent = fs.existsSync(outputPath) ? fs.readFileSync(outputPath, 'utf8') : null;

if (checkOnly) {
  if (currentContent !== expectedContent) {
    console.error(`${outputPath} is out of sync with package manifests.`);
    console.error('Run: pnpm sync:versions');
    process.exit(1);
  }

  console.log(`${outputPath} is up to date.`);
  process.exit(0);
}

fs.writeFileSync(outputPath, expectedContent);
console.log(`Updated ${outputPath}.`);
